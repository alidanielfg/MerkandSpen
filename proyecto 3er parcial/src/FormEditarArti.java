    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        String nuevoNombre = txtNombre.getText().trim();
    String nuevaDescripcion = txtDescripcion.getText().trim();
    String cantidadStr = txtCantidad.getText().trim();
    
    if (nuevoNombre.isEmpty() || nuevaDescripcion.isEmpty() || cantidadStr.isEmpty()) {
        JOptionPane.showMessageDialog(this, 
            "Nombre, descripción y cantidad son campos obligatorios", 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try {
        int nuevaCantidad = Integer.parseInt(cantidadStr);
        if (nuevaCantidad < 0) {
            JOptionPane.showMessageDialog(this, 
                "La cantidad no puede ser un número negativo", 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (articuloDAO.actualizarArticulo(idArticulo, nuevoNombre, nuevaDescripcion, nuevaCantidad)) {
            JOptionPane.showMessageDialog(this, 
                "Artículo actualizado correctamente", 
                "Éxito", 
                JOptionPane.INFORMATION_MESSAGE);
            
            if (inventarioParent != null) {
                inventarioParent.llenarTabla();
            }
            
            this.dispose();
        }
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, 
            "La cantidad debe ser un número entero válido", 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, 
            "Error al actualizar el artículo: " + ex.getMessage(), 
            "Error", 
            JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnActualizarActionPerformed

