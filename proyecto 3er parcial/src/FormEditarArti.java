    private void cargarDatosArticulo() {
        try {
            CRUD.Articulo articulo = articuloDAO.obtenerArticuloPorId(idArticulo);
            if (articulo != null) {
                txtNombre.setText(articulo.getNombre());
                txtDescripcion.setText(articulo.getDescripcion());
                txtCantidad.setText(String.valueOf(articulo.getCantidad()));
                
                String nombreCategoria = articuloDAO.obtenerNombreCategoria(articulo.getIdCategoria());
                txtCategoria.setText(nombreCategoria);
                
                txtCategoria.setEditable(false);
                txtCategoria.setBackground(new Color(240, 240, 240));
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                "Error al cargar datos del artículo: " + ex.getMessage(), 
                "Error", 
                JOptionPane.ERROR_MESSAGE);
        }
    }

